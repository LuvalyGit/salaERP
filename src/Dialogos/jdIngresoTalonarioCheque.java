    public String getFecha() {
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        return( sdf.format( (dtFecha.getDate()).getTime() ) );
        } catch (Exception e) {
            return "";
        }
    }
    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed
       ExeSql Sql = new ExeSql();
       
       if(txInicio.getText().isEmpty() ||
               txFinal.getText().isEmpty() ||
               getFecha().isEmpty()){
           fmMain.Mensaje("Faltan datos");
           return;
       }
       
        try {
            Sql.ExeSql("INSERT INTO cheques_talonarios\n" +
                        "( banco,nroini, nrofin, fecha) VALUES (\n" +
                        BancoId + ",\n" +
                        txInicio.getText().trim() + ",\n" +
                        txFinal.getText().trim() + ",\n'" +
                        getFecha() + "')");
            Sql.Commit();
            fmMain.Mensaje("Guardado");
            dispose();
        } catch (Exception e) {
            Sql.Rollback();
            fmMain.Mensaje(e.getMessage());
        }finally{
            Sql.Close();
        }
    }//GEN-LAST:event_btGuardarActionPerformed

