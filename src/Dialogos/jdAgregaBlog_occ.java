    private void GuardaBlogOCP(){
        ExeSql Sql = new ExeSql();
          int aumenta =1;
        
        try {
          
            if(cbTipo.getSelectedIndex()==2)
            {aumenta = 3;}
            
            if (Orden.equals(null)){
                fmMain.Mensaje("No se puede grabar comentarios");
                return;
            }
                Sql.ExeSql("insert into blog_occ\n" +
                           "(rut, codigo_oc ,orden ,texto, tipo, compromiso, fcompromiso) \n" +
                           "values\n" +
                           "(" + Rut + "," + CodigoOC + ",'" + Orden + "','" + txMensaje.getText() + "'," + (cbTipo.getSelectedIndex()+aumenta) + ",'false','" + getFecha() +"')");
            Sql.Commit();
            Guardar = true;
            dispose();
            
        } catch (Exception e) {
            Guardar = false;
            LogError.Guardar(this.getName(),e.getMessage());
        } finally{
            Sql.Close();
        }
        
    }
