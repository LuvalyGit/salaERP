    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (fmMain.OkCancel("¿Desea realizar la conciliacion?") == JOptionPane.OK_OPTION) {
            ExeSql Sql = new ExeSql();
            try {
                /**
                * **** CAMBIA ESTADO ********
                */
                Sql.ExeSql("UPDATE bco_cartola set asociado=true WHERE id on (" + txIdBanco.getText().trim()+"," + txIdBanco2.getText().trim()+")");
                /**
                * **** ASOCIA ID ********
                */
                

                Sql.Commit();
                fmMain.Mensaje("Movimiento Conciliado");
                dispose();
            } catch (Exception e) {
                System.out.println(e.getMessage());
                Sql.Rollback();
            } finally {
                Sql.Close();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    
