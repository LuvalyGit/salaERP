    public int Show(String Cuenta, BigInteger Monto, String BcoNombre,String CtaNombre){
        
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        DefaultTableModel Model = (DefaultTableModel) Grilla.getModel();
        fmMain.LimpiaGrilla(Model);
        lbBanco.setText(BcoNombre);
        lbCuenta.setText(CtaNombre);
        
        try {
            Rs = Sql.Select( "select ca.fecha,ca.oficina,ca.nrodocumento,ca.detalle,ca.cargo,ca.abono,ca.id\n" +
                        "from bco_cartola ca\n" +
                        "where ca.abono between "+ Monto +" -1 and " + Monto + "+1\n" +
                        "and ca.cuenta=" + Cuenta + " and ca.asociado=false");
           
            while(Rs.next()){
                Model.addRow(new Object[]{
                                Rs.getString("Fecha").trim(),
                                Rs.getString("Oficina").trim(),
                                Rs.getString("nrodocumento").trim(),
                                Rs.getString("detalle").trim(),
                                Rs.getInt("Cargo"),
                                Rs.getInt("abono"),
                                Rs.getInt("id")
                });
            }
            this.setLocationRelativeTo(null);
            this.setVisible(true);
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        } finally{
            Sql.Close();
            return Id;
        }
    }
