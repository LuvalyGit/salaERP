    private String TextoBusqueda(){
        String Texto="upper(p.nombre) like '%";
        String Palabras[] = txBuscar.getText().split(" ");
        
        if(Palabras.length>0){
            Texto = Texto + Palabras[0] + "%' ";
            for(int i=1; i< Palabras.length; i++){
                Texto = Texto + "or upper(p.nombre) like '%" + Palabras[i] + "%' "; 
            }
        }
        else{
            Texto = Texto + txBuscar.getText() + "%' ";         
        }
        return Texto;
    }
    
    
    public void buscar_prod(Boolean cotiza){
        
//      DefaultTableModel tbMd = new DefaultTableModel();
        DefaultTableModel tbMd = (DefaultTableModel) Grilla.getModel();
        
        while (tbMd.getRowCount() > 0) {
        
            tbMd.removeRow(0);
        }
        
        
        ResultSet Rs;
        ExeSql  Sql = new ExeSql();   
        
        
        
       ExeSql Sql2 = new ExeSql();
       ResultSet Rs2;
        
        boolean sala = false;
            
        int cant = 0;
        
        
        

        String ID = "";
//        
//        String sql = "SELECT p.sku,id2.ids,p.nombre,i.stock AS stock,p.notransado,p.desxprecio, p.sinstock,p.convenio,psl.sublinea \n" +
//                     "FROM producto p\n" +                                          
//                     "LEFT JOIN inventario i ON p.sku=i.sku \n" + 
//                     "LEFT JOIN par_sublinea psl ON p.sublinea=psl.codigo \n" +
//                     "LEFT JOIN (SELECT sku, STRING_AGG (idch, ' - ' ORDER BY idch) AS ids \n" +    //Permite agrupar campos en uno solo
//                     "FROM codchile GROUP BY sku) id2 ON (p.sku = id2.sku)" ;
        
        
        
         String sql = "SELECT p.sku,id2.ids,p.nombre,i.stock AS stock,p.notransado,p.desxprecio, p.sinstock,p.publicado,psl.sublinea,p.pventa \n" +
                     "FROM producto p\n" +                                          
                     "LEFT JOIN inventario i ON p.sku=i.sku \n" + 
                     "LEFT JOIN par_sublinea psl ON p.sublinea=psl.codigo \n" +
                     "LEFT JOIN (SELECT sku, STRING_AGG (idch, ' - ' ORDER BY idch) AS ids \n" +    //Permite agrupar campos en uno solo
                     "FROM codchile GROUP BY sku) id2 ON (p.sku = id2.sku)" ;
        
        
        
        if(Insumos)
            sql = sql + "where insumo=true\n";  
        else
            sql = sql + "where insumo=false\n";
        
            sql = sql + "and (" + TextoBusqueda() + ") ";
                
        if ( cotiza ){}

        else
            sql = sql  + " and p.es_cotiza <> 1 \n";

        if(jCheckBox1.isSelected()){
            sql = sql + " and i.stock > 0\n";
        }
        

        if ( cotiza ){
           sql = sql  + " order by nombre";
        }    
        else if (!jCheckBox1.isSelected())
           
            sql = sql  + " order by nombre";
            

        try {
            
            Rs = Sql.Select(sql);
            
            while (Rs.next()){
                 
                ID = Rs.getString("ids");
                notran=Rs.getBoolean("notransado");
                desxpr=Rs.getBoolean("desxprecio");
                nstock=Rs.getBoolean("sinstock");
                String categoria=Rs.getString("sublinea");
                String publica=Rs.getString("publicado");
                double pnormal = (Rs.getDouble("pventa") * 1.19) * 1.05;
                String precnorm = String.valueOf(Math.round(pnormal));
                
                
                if (ID == null){
                
                   ID = " ";
                
                }
                
    //**************************************************************************************************************************************************//            
                
                 Rs2 = Sql2.Select("SELECT mt.sku, case when sum(mt.cant) is null then 0 else sum(mt.cant) end as total FROM mt_productos mt \n" +
                                   "WHERE mt.sku = '"+Rs.getString("sku")+"' AND mt.ubicacion LIKE '%SAL%' \n" +
                                   "GROUP BY mt.sku");
                 
                if (Sql2.GetRowCount() > 0){
                 
                    Rs2.next();
                    
                    cant = Rs2.getInt("total"); 
                    
                    if (cant > 0){
                    
                        sala = true; 
                       
                    
                    }else {
                    
                      sala = false;      
                    
                    }
                 
                }else {
                
                    sala = false; 
                
                }
               
                
    //**************************************************************************************************************************************************//            
                
                
                tbMd.addRow(new Object[]{Rs.getString("sku"),
                                         ID,
                                         Rs.getString("nombre"),
                                         Rs.getString("stock"),
                                         notran,
                                         desxpr,
                                         nstock,
                                         categoria,
                                         publica,
                                         precnorm,
                                         sala
                });
            
            }
            
            Grilla.setDefaultRenderer(Object.class, new TableCellRendererBuscaProducto());
            Grilla.repaint();
            

        } catch (Exception e) {
        }
    }
    
    
    
