    public String getFecha() {
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        return( sdf.format( (dtFecha.getDate()).getTime() ) );
        } catch (Exception e) {
            return "";
        }
    }   
    private void btAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarActionPerformed
        ExeSql Sql = new ExeSql();
        
        try {
            
            if(rbSi.isSelected() && getFecha().isEmpty() ){
                fmMain.Mensaje("Faltan datos");
                return;
            }
            
            if(rbSi.isSelected()){
                Sql.ExeSql("insert into cli_cobranzablog\n" +
                       "(rut,texto, tipo,codigo_oc,fcompromiso, nrodocto)values\n" +
                       "("+ Rut + ",'"+ txMensaje.getText() +"'," + (cbTipo.getSelectedIndex()+1) +","+ Codigo_Oc +",'"+ getFecha() + "',"+nrodocto+")");
            }
            else{
                Sql.ExeSql("insert into cli_cobranzablog\n" +
                       "(rut,texto, tipo,codigo_oc, nrodocto)values\n" +
                       "("+ Rut + ",'"+ txMensaje.getText() +"'," + (cbTipo.getSelectedIndex()+1) +","+ Codigo_Oc +","+nrodocto+")");
            }
            
            Sql.Commit();
            Guardar = true;
            dispose();
        } catch (Exception e) {
            fmMain.Mensaje(e.getMessage());
            Guardar=false;
        }finally{
            Sql.Close();
                    
        }
    }//GEN-LAST:event_btAgregarActionPerformed
