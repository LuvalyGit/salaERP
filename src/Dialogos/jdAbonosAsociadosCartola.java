    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ExeSql sql = new ExeSql();
        ResultSet rs = null;
        
       
        try {
//            String query = "select bc.cargo, bc.abono, pp.monto, pp.fechapago, pp.usuario\n" +
//            "from bco_cartola bc\n" +
//            "left join prv_pagos pp on pp.id_cartola = bc.id\n" +
//            "where bc.id = "+getId()+" and bc.asociado = true";
            
            
            String query = "select bc.cargo, bc.abono, cp.monto, cp.fechapago,cxc.tipdocto,cxc.nrodocto,cxc.rut, cp.usuario\n" +    
                            "from bco_cartola bc\n" +
                            "left join cli_pagos cp on cp.id_cartola = bc.id\n" +
                            "left join cli_cuentasxcobrar cxc on cp.cuenta = cxc.id\n" +
                            "where bc.id = "+getId()+" and (bc.asociado = true or cxc.pagado > 0)"; 
            
            
            
            rs = sql.Select(query);
            DefaultTableModel model = (DefaultTableModel) Grilla.getModel();
            while(model.getRowCount()>0){
                model.removeRow(0);
            }
            for(int i = 0; rs.next(); i++) {
                model.addRow(new Object[]{
                    rs.getString("cargo"),
                    rs.getString("abono"),
                    rs.getString("monto"),
                    rs.getString("fechapago"),
                    rs.getString("tipdocto"),
                    rs.getString("nrodocto"),
                    rs.getString("rut"),
                    rs.getString("usuario")
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(jdAbonosAsociadosCartola.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally {
            sql.Close();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

