    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ExeSql sql = new ExeSql();
        String query = "insert into fechallegadaprv (sku,fecha_llegada,usuario) \n "
                + "values ('"+getSku()+"', '"+getFechaInicio()+"','"+fmMain.GetUsuario()+"') ";
        try {
            int result = sql.ExeSqlInt(query);
            if(result > 0){
                JOptionPane.showMessageDialog(rootPane,"Se guardó fecha llegada.");
                sql.Commit();
            }
            else {
                JOptionPane.showMessageDialog(rootPane,"No se guardó correctamente.");
            }
            
        } catch (SQLException ex) {
            sql.Rollback();
            Logger.getLogger(jdFechaLlegadaProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally {
            sql.Close();
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed
