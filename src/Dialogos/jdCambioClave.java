    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
        if(!txClaveActual.getText().equals(fmMain.GetPass())){
            fmMain.Mensaje("Clave actual incorrecta");
            return;
        }
        if(!txNuevaClave.getText().equals(txConfirmaClave.getText())){
            fmMain.Mensaje("Nuevas claves no coinciden");
            return;
        }
        ExeSql Sql = new ExeSql();
        try {
            Sql.ExeSql("alter user "+ txUsuario.getText().toLowerCase().trim() +" with password '"+ txNuevaClave.getText().trim() +"'");
            Sql.Commit();
            fmMain.Mensaje("Clave cambiada con exito");
            this.dispose();
        } catch (Exception e) {
            LogError.Guardar(this.getClass().getSimpleName(),e.getMessage());
            fmMain.Mensaje("Ocurri√≥ un error");
        }finally{
            Sql.Close();
        }
        
        
            
    }//GEN-LAST:event_btAceptarActionPerformed

