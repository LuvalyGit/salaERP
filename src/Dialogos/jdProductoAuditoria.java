    public void CargarGrilla() {
        String cantidad = cbCantidad.getSelectedItem().toString();
        String Query = "select pa.folio, pa.fecha, pa.ubicacion, p.sku, p.nombre, pa.cant, pa.cant_metro, pa.usuario , pa.usuario_mod\n" +
                        "from mt_producto_auditoria as pa\n" +
                        "left join producto p on p.sku = pa.sku\n";
                        if(!txsku.getText().equals("") && txfolio.getText().equals("")){
                            Query = Query + "where p.sku = '"+txsku.getText()+"' \n";
                        }
                        if(!txfolio.getText().equals("") && txsku.getText().equals("")){
                            Query = Query + "where pa.folio = "+txfolio.getText()+" \n";
                        }
                        if(!txfolio.getText().equals("") && !txsku.getText().equals("")){
                            Query = Query + "where pa.folio = "+txfolio.getText()+" and p.sku = '"+txsku.getText()+"'\n";
                        }
                        
                        if(jrantiguo.isSelected()){
                            Query = Query + "order by pa.fecha asc\n";
                        }
                        else {
                            Query = Query + "order by pa.fecha desc\n";
                        }
                        if (!cantidad.equals("Todos")) {
                            Query = Query + "limit "+cbCantidad.getSelectedItem().toString()+"";
                        }
                        
        try {
            ExeSql sql = new ExeSql();
            ResultSet rs = sql.Select(Query);
            DefaultTableModel model = (DefaultTableModel) Grilla.getModel();
            while(model.getRowCount()>0){
                model.removeRow(0);
            }
            for(int i = 0; rs.next();i++){
                model.addRow(new Object[]{
                    rs.getInt("folio"),
                    rs.getString("fecha"),
                    rs.getString("ubicacion"),
                    rs.getString("sku"),
                    rs.getString("nombre"),
                    rs.getInt("cant"),
                    rs.getInt("cant_metro"),
                    rs.getString("usuario"),
                    rs.getString("usuario_mod")
                });
            }
            TableRowSorter row = new TableRowSorter(model);
            Grilla.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(jdProductoAuditoria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
