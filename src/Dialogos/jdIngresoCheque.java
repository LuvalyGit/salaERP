    public void CargaDatos(String Banco,String CodBanco,String Numero){
        txBanco.setText(Banco.trim());
        BancoId = CodBanco;
        txNroDocto.setText(Numero.trim());
    }
    public void CargaDatosBlanco(String Banco,String CodBanco,String Numero){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        
        try {
            Rs= Sql.Select("select c.rut,p.nombre\n" +
                            "from cheques_emitidos c \n" +
                            "left join proveedor p on c.rut=p.rut\n" +
                            "where numero='"+ Numero +"'");
            Rs.next();
            txRut.setText(Rs.getString("rut").trim());
            txNombre.setText(Rs.getString("nombre").trim());
            txMonto.requestFocus();
        } catch (Exception e) {
            System.out.println(e);
        }finally{
            Sql.Close();
        }
        txBanco.setText(Banco.trim());
        BancoId = CodBanco;
        txNroDocto.setText(Numero.trim());
        txRut.setEditable(false);
        
    }

    
    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed
        
        if(getFEmision().isEmpty() || getFVencimiento().isEmpty() ){
            fmMain.Mensaje("Debe especificar fechas");
            return;
        }
        if(txMonto.getText().isEmpty()){
//            fmMain.Mensaje("Falta MONTO del Cheque");
//            return;
            txMonto.setText("-1");
        }
        if(txNombre.getText().isEmpty()){
            fmMain.Mensaje("Falta DESTINATARIO");
            return;
        }
        
        if (lsFacturas.getModel().getSize() == 0)
        {
          fmMain.Mensaje("Debe ingresar al menos una factura a pagar");
            return;  
        }
        
        ExeSql Sql = new ExeSql();  
        ExeSql Sql2 = new ExeSql();   
        try {    
        
            Sql.ExeSql("update cheques_emitidos set \n" +
                        "rut="+ txRut.getText().trim() +",\n" +
                        "femision='"+ getFEmision() +"',\n" +
                        "fvencimiento='"+ getFVencimiento() +"',\n" +
                        "monto="+ txMonto.getText().trim() +"\n" +
                        "where banco="+ BancoId +"\n" +
                        "and numero='" + txNroDocto.getText().trim() + "'") ;
            
            Sql2.ExeSql("delete from cheques_emitidos_facturas where banco="+ BancoId +"\n" +
                        "and numero='" + txNroDocto.getText().trim() + "'") ;
            
            
            for(int i=0; i< lsFacturas.getModel().getSize(); i++){
                    Sql2.ExeSql ("INSERT INTO cheques_emitidos_facturas \n" +
                            "(banco,numero,nrodocto,rut) \n" +
                            "VALUES ("+BancoId+", '"+txNroDocto.getText().trim()+"'"
                            + ","+lsFacturas.getModel().getElementAt(i)+", "+txRut.getText().trim()+" )");
                }
            
            Sql.Commit();
            Sql2.Commit();
            dispose();
        } catch (Exception e) {
            fmMain.Mensaje(e.getMessage());
            Sql.Rollback();
            Sql2.Rollback();
        }finally{
            Sql.Close();
            Sql2.Close();
        }
    }//GEN-LAST:event_btGuardarActionPerformed

