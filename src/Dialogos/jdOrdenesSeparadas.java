    public void OrdenesSeparadas() {
        ExeSql sql = new ExeSql();
        ResultSet rs = null;
        String Query = "select distinct on (result.orden) result.sku, result.nombre, result.cantidad, result.separado, result.resta, result.orden, result.ubicacion from \n" +
                        "(select \n" +
                        "o.sku,\n" +
                        "p.nombre,\n" +
                        "o.rut,\n" +
                        "o.cantidad, o.separado, o.despachado,\n" +
                        "case when o.separado > 0\n" +
                        "then (o.cantidad - o.separado )\n" +
                        "else o.separado\n" +
                        "end as resta,\n" +
                        "CAST(o.codigo_oc as varchar) || '-' || o.orden as orden,\n" +
                        "case when mt.ubicacion is null then 'Sin Ubicación' else mt.ubicacion end as ubicacion\n" +
                        "from occhdet o\n" +
                        "left join mt_productos2 mt on  mt.sku = o.sku\n" +
                        "left join producto p on o.sku = p.sku\n" +
                        ") result\n" +
                        "where result.resta > 0\n" +
                        "order by result.orden";
        try {
            DefaultTableModel model = (DefaultTableModel) Grilla.getModel();
            while(model.getRowCount()>0){
                model.removeRow(0);
            }
            rs = sql.Select(Query);
            for(int i = 0; rs.next(); i++){
                model.addRow(new Object[]{
                    rs.getString("sku"),
                    rs.getString("nombre"),
                    rs.getInt("cantidad"),
                    rs.getInt("separado"),
                    rs.getInt("resta"),
                    rs.getString("orden"),
                    rs.getString("ubicacion")
                });
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(jdOrdenesSeparadas.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
