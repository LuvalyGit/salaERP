    public void CargaComunas(){
         
        
        DefaultComboBoxModel value;
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        String Query="";
        cbComunas.removeAllItems();
        int i=0;
        value =new DefaultComboBoxModel();
        try {
            Query=" select id,region,comuna, provincia from g_comunas ";
            Query = Query + "order by comuna;";
            Rs = Sql.Select(Query);
            if(Sql.GetRowCount()==0) return;
           value.addElement(new Combo_CodStr("Seleccione","",0));     
           while(Rs.next()){
            value.addElement(new Combo_CodStr(Rs.getString("comuna").trim(),Rs.getString("id").trim(),i));
            System.out.println(Rs.getString("comuna").trim());
            i++;
           } 
           cbComunas.setModel(value);
           cbComunas.setSelectedIndex(0);
         
           

        } catch (Exception e) {
            System.out.println(e);
        }
        finally{
            Sql.Close();
        }
     }
    
    

    
    
    
    
    
            
    
    
