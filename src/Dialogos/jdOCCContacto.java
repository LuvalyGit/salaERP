    public void CargaContacto(String Id){
        ExeSql  Sql = new ExeSql();
        ResultSet Rs;
        
        try{
            Rs = Sql.Select("select nombre,fono,celular,email\n" +
                        "from clicontactopersonas\n" +
                        "where id=" + Id);
            Rs.next();
            txNombre.setText(Rs.getString("nombre").trim());
            txFono.setText(Rs.getString("fono").trim());
            txMail.setText(Rs.getString("email").trim());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }finally{
            Sql.Close();
        }
        
    }
