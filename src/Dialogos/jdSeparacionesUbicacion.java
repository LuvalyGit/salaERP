    public void CargaSeparaciones(String codigo, String cantidad,String ubicacion) {

        ExeSql sql = new ExeSql();
        ResultSet rs = null;
        try {
            System.out.println(codigo);
            String query = "select \n" +
                            "s.sku, \n" +
                            "s.fecha, \n" +
                            "s.cantidad,\n" +
                            "s.codigo_oc || '-' ||s.orden orden,\n" +
                            "s.usuario\n" +
                            "from separacion s\n" +
                            "where s.sku = '"+codigo+"' and s.ubicacion_origen = '"+ubicacion+"'\n" +
                            "order by s.fecha desc";
            
            DefaultTableModel model = (DefaultTableModel) Grilla.getModel();
            fmMain.LimpiaGrilla(model);
            rs = sql.Select(query);
            int conteo = 0;
            for(int i = 0; rs.next(); i++){
                conteo = conteo + rs.getInt("cantidad");
                if(conteo >= Math.abs(Double.valueOf(cantidad))){
                    System.out.println(rs.getString("sku")+" - "+rs.getString("fecha")+" - "+rs.getString("orden")+" - "+rs.getString("usuario")+" - "+rs.getString("cantidad"));
                    model.addRow(new Object[]{
                        rs.getString("sku"),
                        rs.getString("fecha"),
                        rs.getString("orden"),
                        rs.getString("usuario"),
                        rs.getString("cantidad")
                    });
                    break;
                }
                else {
                    model.addRow(new Object[]{
                        rs.getString("sku"),
                        rs.getString("fecha"),
                        rs.getString("orden"),
                        rs.getString("usuario"),
                        rs.getString("cantidad")
                    });
                }
            }
            Grilla.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(jdSeparacionesUbicacion.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally {
            sql.Close();
        }
    }
