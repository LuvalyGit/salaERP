    public void Carga(){
            ExeSql sql = new ExeSql();
            ResultSet rs = null;
            String id = getSku();
        try {
            rs=sql.Select("select imagen from producto where sku = '"+id+"' limit 1");
            rs.next();
            if(rs.getString("imagen")!=null){
                CargaPdf(id, rs.getString("imagen"));
            }
            else {
                JOptionPane.showMessageDialog(null, "No posee archivo de imagen");
            }
        } catch (SQLException ex) {
            Logger.getLogger(pfTicketsLuvaly.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally {
            sql.Close();
        }

    }                                        

