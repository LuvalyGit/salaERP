    public void CargaTabla() {
        ExeSql sql = new ExeSql();
        ResultSet rs = null;
        String Query = "select cta.rut, cta.nrodocto, cta.tipdocto, cta.fcreacion, cta.tipdocorigen from ctacteprv as cta\n";
        if(todos.isSelected()){
            Query = Query + "where cta.tipdocto = 'GDP' or cta.tipdocto = 'FAP'";
        }
        if(gdp.isSelected()){
            Query = Query + "where cta.tipdocto = 'GDP'";
        }
        if(fap.isSelected()){
            Query = Query + "where cta.tipdocto = 'FAP'";
        }
        try {
            rs = sql.Select(Query);
            DefaultTableModel model = (DefaultTableModel) Grilla.getModel();
            while(model.getRowCount()>0){
                model.removeRow(0);
            }
            for(int i = 0; rs.next(); i++){
                model.addRow(new Object[]{
                    rs.getString("rut"),
                    rs.getString("nrodocto"),
                    rs.getString("tipdocto"),
                    rs.getString("tipdocorigen"),
                    rs.getString("fcreacion")
                });
            }
        } 
        catch (SQLException ex) {
            Logger.getLogger(jdBuscarDocumento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
