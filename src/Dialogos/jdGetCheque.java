    public boolean CargaDocumentos(String Rut){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        DefaultTableModel TableModel = (DefaultTableModel) Grilla.getModel();
        
        while(TableModel.getRowCount()>0)
                        TableModel.removeRow(0);  
        
        try {
            Rs = Sql.Select("select b.codigo,b.banco, p.numero,p.monto,p.monto + p.pagado as saldo\n" +
                            "from cheques_emitidos p\n" +
                            "left join par_banco b on p.banco = b.codigo\n" +
                            "where p.rut="+ Rut +"\n" +
                            "and monto>pagado");
            if(Sql.GetRowCount()==0){
                return false;
            }
            
            while(Rs.next()){
                TableModel.addRow(new Object[]{Rs.getString("banco").trim(), 
                                               Rs.getString("numero").trim(), 
                                               fmMain.FormatoNumero(Rs.getDouble("monto")),
                                               fmMain.FormatoNumero(Rs.getDouble("saldo")),
                                               Rs.getString("codigo").trim()});
            }
            return true;
        } catch (Exception e) {
            fmMain.Mensaje(e.getMessage());
            return false;
        }finally{
            Sql.Close();
        }
        
    }
