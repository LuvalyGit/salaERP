    public void Carga() {
        DefaultTableModel model = (DefaultTableModel) Grilla.getModel();
        while(model.getRowCount()>0){
            model.removeRow(0);
        }
        ExeSqlLuvaly luv = new ExeSqlLuvaly();
        ExeSql diso = new ExeSql();
        ResultSet producto_luv = null, producto_dis = null;
        String query = "select sku,nombre from producto where es_cotiza = 0";
        
        try {
            String skus = "";
            producto_dis = diso.Select(query);
            for(int i = 0; producto_dis.next(); i++) {
                skus = skus + "'"+producto_dis.getString("sku").trim()+ "',";
            }
            String final_sku = skus.substring(0, skus.length() - 1);
            String query_2 = "select sku,nombre from producto where es_cotiza = 0 and sku not in ("+final_sku+")";
            producto_luv = luv.Select(query_2);
            int conteo = 0;
            for(int o = 0; producto_luv.next(); o++){
                conteo++;
                model.addRow(new Object[]{
                    producto_luv.getString("sku"),producto_luv.getString("nombre")
                });
            }
            System.out.println("Cantidad: "+conteo);
        } catch (SQLException ex) {
            Logger.getLogger(jdLuvaly.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            luv.Close();
            diso.Close();
        }
        Grilla.setModel(model);
       
        
        
    }
