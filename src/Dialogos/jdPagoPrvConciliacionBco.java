    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (fmMain.OkCancel("Â¿Los datos estan correctos?") == JOptionPane.OK_OPTION) {
            ExeSql Sql = new ExeSql();
            try {
                /**
                 * **** Genera el pago ********
                 */
                 Sql.ExeSql("insert into prv_pagos\n" +
                        "(cuenta,tipopago,banco,monto,nrodocumento,fechapago, id_cartola)\n" +
                        "values\n" +
                       "("+ txIdAsiento.getText().trim() +", 2, 1," + txCargo.getText().trim() +"," + txNroDoctoBanco.getText().trim()+",'" + txFechaBanco.getText().trim() + "','" + txIdBanco.getText().trim() + "')");
                 
                /**
                 * **** CAMBIA ESTADO Cartola ********
                 */
                Sql.ExeSql("UPDATE bco_cartola set asociado=true WHERE id=" + txIdBanco.getText().trim());

                /**
                 * **** ASOCIA ID de cartola al pago********
                 */
                //Sql.ExeSql("UPDATE prv_pagos set id_cartola=" + txIdBanco.getText().trim() + "WHERE id=" + txIdAsiento.getText().trim());

                Sql.Commit();
                fmMain.Mensaje("Pago realizado");
                dispose();
            } catch (Exception e) {
                System.out.println(e.getMessage());
                Sql.Rollback();
            } finally {
                Sql.Close();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

