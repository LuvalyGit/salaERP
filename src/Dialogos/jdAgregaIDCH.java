    private void btGuardaIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardaIDActionPerformed
        // TODO add your handling code here:
        
        
        if (lbSku.getText().toString().trim().equals("")){
            
            fmMain.Mensaje("Imposible guardar, revise que el codigo sea valido: ");
            return;
        }
        
        if (txIDChilecompras.getText().toString().trim().equals("") || txCantidad.getText().toString().trim().equals("") ){
            
            fmMain.Mensaje("Debe Ingresar el ID o la Cantidad: ");
            return;
        }
        
        if (txCantidad.getText().toString().trim().equals("0") ){
            
            fmMain.Mensaje("La Cantidad mínima debe ser 1");
            return;
        }
        
        
        
        ResultSet Rs;
        ExeSql Sql = new ExeSql();
        
        try{
              
        Sql.ExeSql("INSERT into codchile (sku,idch,cant_unidad,usuario) VALUES ("
                        + "'" + lbSku.getText().toString().trim() + "',"
                        + "'" + txIDChilecompras.getText().toString().trim() + "',"
                        + txCantidad.getText().toString().trim() + ","
                        + "'" + fmMain.GetUsuario() + "')");
                    
        Sql.Commit();
        fmMain.Mensaje("ID "+txIDChilecompras.getText().toString().trim()+" asociada correctamente a: "+lbSku.getText().toString().trim());
        
        }
        catch (Exception e){  
            Sql.Rollback();
            fmMain.Mensaje("Error al guardar: "+e);
        }
        finally{
            Sql.Close();
            dispose();
        }
    }//GEN-LAST:event_btGuardaIDActionPerformed
    
