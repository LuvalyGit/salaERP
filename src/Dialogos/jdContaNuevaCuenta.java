    public void CargaPantalla(String ElPadre, int ElNivel){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        Nivel=ElNivel;
        Padre=ElPadre;
        
        if(Nivel==2){
            txNombre2.setEditable(true);
            txNombre2.setBackground(Color.yellow);
            txNombre2.requestFocus();
        }
        if(Nivel==3){
            txNombre3.setEditable(true);
            txNombre3.setBackground(Color.yellow);
            txNombre3.requestFocus();
        }
        if(Nivel==4){
            txNombre4.setEditable(true);
            txNombre4.setBackground(Color.yellow);
            txNombre4.requestFocus();
        }
        
        try {
            Rs = Sql.Select("WITH RECURSIVE q AS(\n" +
                            "	SELECT p.codigo,p.nivel,p.nombre,p.padre\n" +
                            "      FROM conta_plan p\n" +
                            "     WHERE p.codigo='"+ Padre.trim() +"' \n" +
                            "     UNION ALL\n" +
                            "    SELECT p.codigo,p.nivel,p.nombre,p.padre\n" +
                            "      FROM conta_plan p\n" +
                            "      JOIN q ON q.padre=p.codigo\n" +
                            "      )\n" +
                            "      SELECT * FROM q order by q.nivel");
            while (Rs.next()){
                if(Rs.getInt("nivel")==1){
                    txCod1.setText(Rs.getString("codigo").trim());
                    txNombre1.setText(Rs.getString("nombre").trim());
                }
                if(Rs.getInt("nivel")==2){
                    txCod2.setText(Rs.getString("codigo").trim());
                    txNombre2.setText(Rs.getString("nombre").trim());
                }
                if(Rs.getInt("nivel")==3){
                    txCod3.setText(Rs.getString("codigo").trim());
                    txNombre3.setText(Rs.getString("nombre").trim());
                }
                if(Rs.getInt("nivel")==4){
                    txCod4.setText(Rs.getString("codigo").trim());
                    txNombre4.setText(Rs.getString("nombre").trim());
                }
            }
            
        } catch (Exception e) {
        } finally{
            Sql.Close();
        }
    }
