    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
        ExeSql Sql = new ExeSql();
        String Nombre;
        
        
        
        if(Nivel==2)
            Nombre=txNombre2.getText().trim();
        else if(Nivel==3)
            Nombre=txNombre3.getText().trim();
        else
            Nombre=txNombre4.getText().trim();
        
        if(Nombre.isEmpty()){
            JOptionPane.showMessageDialog(this,"Error");
            return;
        }
        try {
            Sql.ExeSql("insert into conta_plan (nivel,padre,nombre) values (" + String.valueOf(Nivel).trim() + ",'" + Padre.trim() + "','" + Nombre +"')");
            Sql.Commit();
            dispose();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        } finally {
            Sql.Close();
        }
    }//GEN-LAST:event_btAceptarActionPerformed

