    public void CargaTipos(String Tipo){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        ElTipo = Tipo;
        cbTipo.removeAllItems();
        cbTipoId.removeAllItems();
        
        try {
            Rs = Sql.Select("select CODIGO,NOMBRE\n" +
                            "from PAR_GENERAL\n" +
                            "WHERE TIPO='"+ Tipo +"' order by codigo");
            while(Rs.next()){
                cbTipoId.addItem(Rs.getString("CODIGO").trim());
                cbTipo.addItem(Rs.getString("NOMBRE").trim());
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        } finally{
            Sql.Close();
        }
    }
