    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
        if(txNombre.getText().isEmpty()){
            fmMain.Mensaje("Debe escribir un nombre");
            return;
        }
        ExeSql Sql = new ExeSql();
        try {
            if(ElTipo.equals("TIPO_GASTOS")){
                Sql.ExeSql("insert into gastos_det (nombre,plan) values ('" + txNombre.getText().trim() + "'," + cbTipoId.getSelectedItem().toString().trim() + ")");
            }
            else{
                Sql.ExeSql("insert into activos_det (nombre,plan) values ('" + txNombre.getText().trim() + "'," + cbTipoId.getSelectedItem().toString().trim() + ")");
            }
            
            
            
            Sql.Commit();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }finally{
            Sql.Close();
        }
        dispose();
        
    }//GEN-LAST:event_btAceptarActionPerformed

