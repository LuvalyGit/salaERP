    public void CargaComboPrv(){
        ResultSet Rs;
        ExeSql Sql = new ExeSql();
        try{
            Rs = Sql.Select("select p.rut, l.nombre, l.apellidopaterno from pacceso p\n" +
                            "left join personal l on p.rut=l.rut\n" +
                            "left join personal_ccostos pc on p.rut=pc.rut\n" +
                            "where p.adminocp=true and pc.ccosto=6 and l.activo=true");
            cbNombres.addItem("Seleccione Firma");
            while (Rs.next())
                { 
                 cbNombres.addItem(Rs.getString("nombre")+" "+Rs.getString("apellidopaterno"));
                }
        }
        catch (Exception e)
        {
         fmMain.Mensaje("Error al cargar nombres: "+e);
         
        }
        finally{
            Sql.Close();
        }
}
