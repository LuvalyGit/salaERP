    public  void CargaCombo( String Query ){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        cbNombres .removeAllItems();
        cbNombres.addItem("<Seleccione>");  // Agrego la condicion inicial de Todos
        try {
            Rs = Sql.Select(Query);
            if(Sql.GetRowCount()==0) return;
     
           while(Rs.next()){
            cbNombres.addItem( Rs.getString("nombre").trim());
            //cbvenId.addItem(Rs.getInt("cod"));   
            //btBuscar.doClick();
           } 
           cbNombres.setSelectedIndex(0);
        } catch (Exception e) {
            System.out.println(e);
        }
        finally{
            Sql.Close();
        }
    }
