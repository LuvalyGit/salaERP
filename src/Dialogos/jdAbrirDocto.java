    private void CargaDoc(){
        DefaultComboBoxModel value;
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        String Query="";
        CboDoc.removeAllItems();
        
//        CboProveedor.addItem("<Todos>");  
//        CboProveedor.setSelectedIndex(0);
        value =new DefaultComboBoxModel();
       
        
        
        try {
            Query="select tipo, nrodoc, nombre from tipo_doc\n" +
                    " order by tipo;";
            Rs = Sql.Select(Query);
            if(Sql.GetRowCount()==0) return;
           while(Rs.next()){
            value.addElement(new Combo_CodStr(Rs.getString("nombre").trim(),Rs.getString("tipo").trim(),Rs.getInt("nrodoc")));
            System.out.println(Rs.getString("tipo").trim());
           } 
           value.addElement(new Combo_CodStr("Factura Transporte","FAT",100));
           CboDoc.setModel(value);
           CboDoc.setSelectedIndex(2);

           
           
        } catch (Exception e) {
            System.out.println(e);
        }
        finally{
            Sql.Close();
        }
    }
    
