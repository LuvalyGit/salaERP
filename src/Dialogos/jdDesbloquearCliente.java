    private void btDesbloquearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDesbloquearActionPerformed
        if (fmMain.OkCancel("¿Desbloquear para esta orden de compra?") == JOptionPane.OK_OPTION) {
            ExeSql Sql = new ExeSql();

            try {
                Sql.ExeSql("insert into cli_desbloqueo\n"
                        + "(rut,codigo_oc,orden,observacion) values(\n"
                        + txRut.getText().trim() + ",\n"
                        + txCodigo_Oc.getText().trim() + ",\n"
                        + "'" + cbOrden.getSelectedItem().toString().trim() + "',\n"
                        + "'" + txObservacion.getText().trim() + "')");
                Sql.Commit();
                fmMain.Mensaje("Desbloqueado");
                dispose();
            } catch (Exception e) {
                System.out.println(e.getMessage());
            } finally {
                Sql.Close();
            }
        }
    }//GEN-LAST:event_btDesbloquearActionPerformed

