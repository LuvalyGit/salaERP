    public void CargaDatos(String Rut,String Nombre,String Codigo_Oc){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        
        try {
            
            txRut.setText(Rut);
            txNombre.setText(Nombre);
            txCodigo_Oc.setText(Codigo_Oc);
            
            Rs = Sql.Select("select orden\n" +
                            "from occh \n" +
                            "where rut="+ Rut +"\n" +
                            "and codigo_oc="+ Codigo_Oc +"\n" +
                            "and estado in (0,1)");
            cbOrden.removeAllItems();
            while(Rs.next()){
                cbOrden.addItem(Rs.getString("orden").trim());
            }
        } catch (Exception e) {
        }finally{
            Sql.Close();
        }
    }
