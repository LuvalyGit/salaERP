    private String getFecha() {
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        return( sdf.format( (dtFecha.getDate()).getTime() ) );
        } catch (Exception e) {
            return "";
        }
    }
    private void GuardaBlogOCP(){
        ExeSql Sql = new ExeSql();
          int aumenta =1;
        
        try {
          
            if(cbTipo.getSelectedIndex()==2)
            {aumenta = 3;}
            
            if (NroDocto.equals(null)){
                fmMain.Mensaje("No se puede grabar comentarios, debe grabar para obtener Número.");
                return;
            }
            
            if(chkCompromiso.isSelected())
                Sql.ExeSql("insert into blog_ocp\n" +
                           "(rut,tipdocto,nrodocto,texto,tipo,compromiso,fcompromiso) \n" +
                           "values\n" +
                           "(" + Rut + ",'" + Tipo + "'," + NroDocto + ",'" + txMensaje.getText() + "'," + (cbTipo.getSelectedIndex()+aumenta) + ",'true','" + getFecha() +"')");
            else
                Sql.ExeSql("insert into blog_ocp\n" +
                           "(rut,tipdocto,nrodocto,texto,tipo,compromiso) \n" +
                           "values\n" +
                           "(" + Rut + ",'" + Tipo + "'," + NroDocto + ",'" + txMensaje.getText() + "'," + (cbTipo.getSelectedIndex()+aumenta) + ",'false')");
            Sql.Commit();
            Guardar = true;
            dispose();
            
        } catch (Exception e) {
            Guardar = false;
            LogError.Guardar(this.getName(),e.getMessage());
        } finally{
            Sql.Close();
        }
        
    }
    private void btAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarActionPerformed
        if(Tipo == "NPP" || Tipo == "CCL")
            GuardaBlogOCP();
        dispose();
        
    }//GEN-LAST:event_btAgregarActionPerformed

