    public void CargaProveedor() {
        ExeSql exe = new ExeSql();
        ResultSet rs = null;
        String sql;
        sql =   "select rut, nombre\n" +
        "from proveedor P";
        try {
            rs = exe.Select(sql);
            DefaultTableModel model = (DefaultTableModel) Grilla.getModel();
            while(model.getRowCount()>0){
                model.removeRow(0);
            }
            for(int i = 0; rs.next();i++){
                model.addRow(new Object[]{
                    rs.getString("rut"),
                    rs.getString("nombre").toUpperCase(),
                    false
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(jdMultipleProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally {
            exe.Close();
        }

    }
    
