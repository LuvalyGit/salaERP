    private void CargaClientes(){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        DefaultTableModel Model = (DefaultTableModel) Grilla.getModel();
        
        fmMain.LimpiaGrilla(Model);
        
        try {
            Rs = Sql.Select("select cxc.rut,c.nombre,\n" +
                            "(select sum(monto-pagado) from cli_cuentasxcobrar where rut=cxc.rut)as Deuda,\n" +
                            "(select sum(monto-pagado) from cli_cuentasxcobrar where rut=cxc.rut and current_date - femision > 30)as Vencido\n" +
                            "from cli_cuentasxcobrar cxc\n" +
                            "left join cliente c on cxc.rut=c.rut\n" +
                            "where cxc.monto - cxc.pagado >10\n" +
                            "and cxc.rut not in (select cliente from cli_cartera)\n" +
                            "group by cxc.rut,c.nombre\n" +
                            "order by Vencido desc");
            
            lbInfo.setText(String.valueOf(Sql.GetRowCount()) + " Registros");
            
            while(Rs.next()){
                Model.addRow(new Object[]{
                        Rs.getString("rut").trim(),
                        Rs.getString("nombre").trim(),
                        fmMain.FormatoTotal(Rs.getDouble("Deuda")),
                        fmMain.FormatoTotal(Rs.getDouble("Vencido"))
                });
            }
            
        } catch (Exception e) {
            Sql.Rollback();
            fmMain.Mensaje(e.getMessage());
        } finally{
            Sql.Close();
        }
    }
    
