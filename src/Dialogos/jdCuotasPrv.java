    public void CargaPagos(String Rut,String NroDocto,String Nombre){
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-yyyy");
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        int Cont=0;
        DefaultTableModel dfTableModel = (DefaultTableModel) Grilla.getModel();
       txRut.setText(Rut);
       txNombre.setText(Nombre);
       txNroDocto.setText(NroDocto);
       
        try {
            Rs =Sql.Select( "select fpago,monto,case estado when 0 then 'Pendiente' else 'Pagado' end as Estado\n" +
                            "from prv_cuentasxpagar\n" +
                            "where rut="+ Rut +"\n" +
                            "and nrodocto="+ NroDocto +"\n" +
                            "order by fpago asc");
            while(Rs.next()){
                Cont++;
                dfTableModel.addRow(new Object[]{   Cont,
                                                    sdf.format(Rs.getDate("fpago")), 
                                                    fmMain.FormatoNumero(Rs.getDouble("monto")),
                                                    Rs.getString("Estado")});
                
            }
        } catch (Exception e) {
            System.out.println(e);
        }finally{
            Sql.Close();
        }
       
       
               
    }
