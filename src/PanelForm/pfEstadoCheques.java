    private void CargaDoctos(String Banco,String Documento){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        DefaultTableModel TableModel = (DefaultTableModel) GrillaPagos.getModel();
        
        while(TableModel.getRowCount()>0)
                        TableModel.removeRow(0);  
        
        try {
            Rs = Sql.Select("select c.nrodocto,p.monto\n" +
                            "from prv_pagos p\n" +
                            "left join prv_cuentasxpagar c on c.id=p.cuenta\n" +
                            "where p.banco="+ Banco +"\n" +
                            "and p.tipopago=3\n" +
                            "and p.nrodocumento='"+ Documento +"'");
            while(Rs.next()){
                TableModel.addRow(new Object[]{"Factura", 
                                                Rs.getString("nrodocto").trim(), 
                                                fmMain.FormatoTotal(Rs.getDouble("monto"))
                                                });
            }
        } catch (Exception e) {
            fmMain.Mensaje(e.getMessage());
        }finally{
            Sql.Close();
        }
    }
    
    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        String Query="";
        DefaultTableModel TableModel = (DefaultTableModel) GrillaCheques.getModel();
        BigInteger SumMontos = new BigInteger("0"); 
        int Cont=0;
        
        while(TableModel.getRowCount()>0)
                        TableModel.removeRow(0);  
        
        try {
            Query = "select c.numero,c.femision,p.nombre,c.fvencimiento,c.monto,c.pagado,c.monto-c.pagado as saldo,cobrado\n" +
                    "from cheques_emitidos c\n" +
                    "left join proveedor p on p.rut=c.rut\n" +
                    "where c.banco=" + cbBancoId.getSelectedItem().toString();
            
            if(cbFecha.getSelectedIndex()==0)
                Query = Query + " and c.femision BETWEEN '"+ getDesde() +"' and '"+ getHasta() +"'";
            else
                Query = Query + " and c.fvencimiento BETWEEN '"+ getDesde() +"' and '"+ getHasta() +"'";
            
            System.out.println(Query);
            Rs = Sql.Select(Query);
            
            while(Rs.next()){
                SumMontos = SumMontos.add(new BigInteger(Rs.getString("monto"))) ;
                Cont++;
                TableModel.addRow(new Object[]{Rs.getString("numero").trim(), 
                                               Rs.getString("femision").trim(), 
                                               Rs.getString("nombre").trim(), 
                                               Rs.getString("fvencimiento").trim(), 
                                               fmMain.FormatoTotal(Rs.getDouble("monto")),
                                               fmMain.FormatoTotal(Rs.getDouble("pagado")),
                                               fmMain.FormatoTotal(Rs.getDouble("saldo")),
                                               Rs.getBoolean("cobrado")
                                                });
            }
            lbCont.setText(String.valueOf(Cont));
            lbSuma.setText(fmMain.FormatoNumeroBig(SumMontos));
            
            
        } catch (Exception e) {
            fmMain.Mensaje(e.getMessage());
        }finally{
            Sql.Close();
        }
    }//GEN-LAST:event_btBuscarActionPerformed

