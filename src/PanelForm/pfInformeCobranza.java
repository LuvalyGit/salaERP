    private void CargaDias(){
        for(int i=1 ; i <=31 ; i++){
            GrillaDias.setValueAt(i, i-1, 0);
            GrillaDias.setValueAt(0, i-1, 1);
        }
    }
    private void CargaDetalle(){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        DefaultTableModel TableModel = (DefaultTableModel) GrillaDet.getModel();
        
        while(TableModel.getRowCount()>0)
            TableModel.removeRow(0); 
        
        try {
            Rs = Sql.Select("select c.rut,cxc.codigo_oc,c.nombre,cxc.nrodocto,p.monto\n" +
                            "from cli_pagos p\n" +
                            "left join cli_cuentasxcobrar cxc on p.cuenta = cxc.id\n" +
                            "left join cliente c on c.rut=cxc.rut\n" +
                            "where EXTRACT(year from fingreso)="+ cbAgno.getSelectedItem().toString().trim() +"\n" +
                            "and EXTRACT(month from fingreso)="+ (cbMes.getSelectedIndex()+1) +"\n" +
                            "and EXTRACT(day from fingreso)=" + GrillaDias.getValueAt(GrillaDias.getSelectedRow(), 0).toString().trim());
            while(Rs.next()){
                 TableModel.addRow(new Object[]{Rs.getString("rut").trim(), 
                                               Rs.getString("codigo_oc").trim(), 
                                               Rs.getString("nombre").trim(),
                                               Rs.getString("nrodocto").trim(),
                                               Rs.getDouble("monto")
                                               });
                
            }
        } catch (Exception e) {
        }finally{
            Sql.Close();
        }
    }
    private void CargaCobros(){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        BigInteger SumMontos = new BigInteger("0"); 
        
        CargaDias();
        
        
        
        try {
            Rs = Sql.Select("select extract(day from fingreso) as dia, sum(monto) as monto\n" +
                            "from cli_pagos\n" +
                            "where EXTRACT(year from fingreso)="+ cbAgno.getSelectedItem().toString().trim() +" \n" +
                            "and EXTRACT(month from fingreso)=" + (cbMes.getSelectedIndex() + 1) + "\n" +
                            "group by fingreso");
            
            while(Rs.next()){
                SumMontos = SumMontos.add(new BigInteger(Rs.getString("monto"))) ;
                GrillaDias.setValueAt(Rs.getDouble("monto"), Rs.getInt("dia")-1, 1);
            }
            txTotalMes.setText(fmMain.FormatoNumeroBig(SumMontos));
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }finally{
            Sql.Close();
        }
    }

