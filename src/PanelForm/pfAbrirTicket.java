public boolean CheckIfSame() {
        boolean mismo = false;
        ExeSql sql = new ExeSql();
        ResultSet rs = null;
        String usuario = fmMain.GetUsuario();
        
        String query = "select usuario from usuario where nombre = '"+usuario_combo.getSelectedItem().toString().trim()+"'\n"
                + "and trim(usuario) = '"+usuario.trim()+"'";
        try {
            rs = sql.Select(query);
            int cantidad = 0;
            for(int i = 0;rs.next();i++){
                cantidad = cantidad + 1;
            }
            if(cantidad == 0){
                System.out.println("Son distintos".toUpperCase().trim());

                mismo = false;
            }
            else {
                System.out.println("Son iguales".toUpperCase().trim());
                mismo = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(pfTicketsLuvaly.class.getName()).log(Level.SEVERE, null, ex);
        }
        return mismo;
    }
    public void GetUser(){
        ExeSql sql = new ExeSql();
        String Query = "select pg.usename, usu.nombre nombre, pcc.nombre area\n" +
                        "from pg_user pg \n" +
                        "left join usuario usu on usu.usuario = UPPER(pg.usename)\n" +
                        "left join personal p on p.usuario = usu.usuario\n" +
                        "left join personal_ccostos pc on pc.rut = p.rut\n" +
                        "left join pccostos pcc on pcc.id = pc.ccosto\n" +
                        "where pg.usename='"+fmMain.GetUsuario().toLowerCase()+"'";
        ResultSet rs = null;
        try {
            rs = sql.Select(Query);
            for(int i = 0;rs.next();i++){
                label_usuario.setText(rs.getString("nombre"));
                label_area.setText(rs.getString("area"));
            }
            rs = null;
            
            
        } catch (SQLException ex) {
            Logger.getLogger(pfTicketsLuvaly.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
