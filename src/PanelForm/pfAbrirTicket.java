    public void InsertarTicket() {
        ExeSql sql = new ExeSql();
        String área = area_combo.getSelectedItem().toString().trim();
        String destinatario = usuario_combo.getSelectedItem().toString().trim();
        String asunto = asunto_text.getText().trim();
        int prioridad = prioridad_combo.getSelectedIndex()+1;
        String remitente = fmMain.GetUsuario().trim();

        try {
            int resultado = sql.ExeSqlInt("insert into tickets_luvaly \n" +
                                            "(estado,remitente, destinatario, fecha_creacion,fecha_cierre,asunto,prioridad)\n" +
                                            "values \n" +
                                            "(0,'"+remitente+"',(select usuario from usuario where nombre = '"+destinatario+"'),\n" +
                                            "now(),null,'"+asunto+"',"+prioridad+")");
            if(resultado>0){
                JOptionPane.showMessageDialog(null, "Se creó ticket con éxito.");
                sql.Commit();
            }
            else {
                JOptionPane.showMessageDialog(null, "Error al crear ticket.");
            }
            
        } catch (SQLException ex) {
            sql.Rollback();
            Logger.getLogger(pfTicketsLuvaly.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally {
            sql.Close();
        }
        
    }

