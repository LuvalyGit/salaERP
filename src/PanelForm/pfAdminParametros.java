    private void CargaDatos(){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        
        try {
            Rs = Sql.Select("select valor\n" +
                            "from par_general\n" +
                            "where tipo='UMBRALDEMARGEN'\n" +
                            "and codigo=0");
            Rs.next();
            txValor.setText(Rs.getString("valor").trim());
        } catch (Exception e) {
            
        }
    }

