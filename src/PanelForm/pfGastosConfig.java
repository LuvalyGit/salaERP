    private void CargaGastosDetalles(){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        DefaultTableModel Model = (DefaultTableModel) GrillaCCostos.getModel();
        
        fmMain.LimpiaGrilla(Model);
        
        try {
            CargaCCostos();
            //Carga Cuenta Contable y detalles
            Rs = Sql.Select("select p.codigo,p.nombre, d.sinrut,d.comentarios,vigente\n" +
                            "from gastos_det d\n" +
                            "join conta_plan p on d.cuenta=p.codigo\n" +
                            "where d.id=" + GrillaGastos.getValueAt(GrillaGastos.getSelectedRow(),0).toString().trim());
            if(Sql.GetRowCount()>0){
                Rs.next();
                txCtaContableCod.setText(Rs.getString("codigo").trim());
                txCtaContable.setText(Rs.getString("nombre").trim());
                chkSinRut.setSelected(Rs.getBoolean("sinrut"));
                chkVigente.setSelected(Rs.getBoolean("vigente"));
                txComentarios.setText(Rs.getString("comentarios").trim());
            }
            else{
                txCtaContableCod.setText("");
                txCtaContable.setText("");
            }
            
            
        } catch (Exception e) {
        }finally{
            Sql.Close();
        }
        
        
    }
    private void CargaCCostos(){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        DefaultTableModel Model = (DefaultTableModel) GrillaCCostos.getModel();
        
        fmMain.LimpiaGrilla(Model);
        
        try {
            Rs = Sql.Select("select cc.id, cc.nombre\n" +
                            "from gastos_link l\n" +
                            "left join pccostos cc on l.ccosto=cc.id\n" +
                            "where l.gastosdet=" + GrillaGastos.getValueAt(GrillaGastos.getSelectedRow(), 0).toString().trim());
            while(Rs.next()){
                Model.addRow(new Object[]{
                            Rs.getString("id").trim(),
                            Rs.getString("nombre").trim()
                });
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }finally{
            Sql.Close();
        }
    }
    private void CargaGastos(){
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        DefaultTableModel Model = (DefaultTableModel) GrillaGastos.getModel();
        
        fmMain.LimpiaGrilla(Model);
        
        try {
            Rs = Sql.Select("select id,nombre\n" +
                            "from gastos_det order by nombre");
            while(Rs.next()){
                Model.addRow(new Object[]{
                            Rs.getString("id").trim(),
                            Rs.getString("nombre").trim()
                });
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }finally{
            Sql.Close();
        }
    }

