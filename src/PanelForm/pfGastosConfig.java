    private void btAgregarCCostoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarCCostoActionPerformed
        String Query="";
        String Codigos = "";

        if(GrillaCCostos.getRowCount()==0)
        Query = "select id as codigo, nombre\n" +
        "from pccostos";
        else{
            for(int i=0; i< GrillaCCostos.getRowCount();i++){
                Codigos = Codigos + GrillaCCostos.getValueAt(i,0).toString().trim() + ",";
            }
            if(Codigos.length()>0)
            Codigos = Codigos.substring(0, Codigos.length()-1);
            Query = "select id as codigo,nombre\n" +
                    "from pccostos where id not in ("+ Codigos +")";
        }        // TODO add your handling code here:

        jdAgregaDato Dato = new jdAgregaDato(null, true);
        Dato.setLocationRelativeTo(null);
        Dato.setTitle("Asocia C. Costos");
        Dato.CargaDatos(Query);
        Dato.setVisible(true);
        if(!Dato.GetCodigo().isEmpty()){
            ExeSql Sql = new ExeSql();
            try {
                Sql.ExeSql("insert into gastos_link (ccosto,gastosdet) values (" + Dato.GetCodigo() + "," + GrillaGastos.getValueAt(GrillaGastos.getSelectedRow(),0).toString().trim() + ")");
                Sql.Commit();
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }finally{
                Sql.Close();
                CargaCCostos();
            }

        }
    }//GEN-LAST:event_btAgregarCCostoActionPerformed

    private void btCtaContableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCtaContableActionPerformed
        String Query="";
        
        Query = "select codigo,nombre\n" +
                "from conta_plan\n" +
                "where codigo like '3%'\n" +
                "and codigo not like '30%' and nivel=4 order by nombre";
        
        jdAgregaDato Dato = new jdAgregaDato(null, true);
        Dato.setLocationRelativeTo(null);
        Dato.setTitle("Cuenta Contable");
        Dato.CargaDatos(Query);
        Dato.setVisible(true);
        
        if(!Dato.GetCodigo().isEmpty()){
            ExeSql Sql = new ExeSql();
            try {
                Sql.ExeSql("update gastos_det\n" +
                           "set cuenta='"+ Dato.GetCodigo().trim() +"'\n" +
                           "where id=" + GrillaGastos.getValueAt(GrillaGastos.getSelectedRow(), 0).toString().trim());
                Sql.Commit();
                txCtaContableCod.setText(Dato.GetCodigo().trim());
                txCtaContable.setText(Dato.GetNombre().trim());
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }finally{
                Sql.Close();
            }

        }
    }//GEN-LAST:event_btCtaContableActionPerformed

