    private void btAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarActionPerformed
        DefaultTableModel Model = (DefaultTableModel) Grilla.getModel();
        
        //Validaciones
        if(cbSubCategoria.getSelectedIndex()<0 ||
                cbCategoria.getSelectedIndex()<0 ||
                cbTipDocto.getSelectedIndex() < 0 ||
                txNombre.getText().isEmpty() ||
                txRut.getText().isEmpty() ||
                txNroDocto.getText().isEmpty() ||
                dtEmision.getDate() == null ||
                txNeto.getText().isEmpty() ||
                txIVA.getText().isEmpty() ||
                txMonto.getText().isEmpty()){
            fmMain.Mensaje("Faltan Datos");
            return;
        }
            
        String TipDocto;
        
        if(cbTipDocto.getSelectedIndex()==0)
            TipDocto="BOG";
        else
            TipDocto="FAG";
        
        Model.addRow(new Object[]{
                cbSubCategoria.getSelectedItem().toString().trim(),
                txNombre.getText().trim(),
                getFechaAsString(),
                cbTipDocto.getSelectedItem().toString().trim(),
                txNroDocto.getText().trim(),
                Integer.valueOf(txNeto.getText()),
                Integer.valueOf(txIVA.getText()),
                Integer.valueOf(txMonto.getText()),
                cbCategoriaId.getSelectedItem().toString().trim(),
                cbSubCategoriaId.getSelectedItem().toString().trim(),
                txRut.getText().trim(),
                TipDocto
        });
        TotalGral = TotalGral + Double.valueOf(txMonto.getText());
        txTotal.setText(fmMain.FormatoTotal(TotalGral));
        
        cbCategoria.setSelectedIndex(-1);
        cbSubCategoria.setSelectedItem(-1);
        txRut.setText("");
        txNombre.setText("");
        txNroDocto.setText("");
        txMonto.setText("");
        txNeto.setText("");
        txIVA.setText("");
        cbTipDocto.setSelectedIndex(-1);
        
    }//GEN-LAST:event_btAgregarActionPerformed

