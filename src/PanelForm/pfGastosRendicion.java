    private void AbrirRendicion(String Folio) {
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        DefaultTableModel Model = (DefaultTableModel) Grilla.getModel();
        
        try {
            Rs = Sql.Select("select *\n" +
                            "from gastosrendicion\n" +
                            "where folio=" + Folio);
            if(Sql.GetRowCount()>0){
                Rs.next();
                lbFolio.setText(Folio);
                if(Rs.getInt("Estado")==0){
                    lbEstado.setText("PENDIENTE");
                    lbEstado.setForeground(Color.RED);
                    btAutorizar.setEnabled(true);
                    btEditar.setEnabled(true);
                }
                else{
                    lbEstado.setText("AUTORIZADA");
                    lbEstado.setForeground(new Color(0,102,0));
                    btAutorizar.setEnabled(false);
                    btEditar.setEnabled(false);
                }
                txComentarios.setText(Rs.getString("comentarios"));
                txTotal.setText(fmMain.FormatoTotal(Rs.getDouble("total")));
                TotalGral = Rs.getDouble("total");
                
            }
            Rs = Sql.Select("select gd.nombre as concepto,p.nombre,d.femision,case d.tipdocto when 'BOG' then 'Boleta' else 'Factura' end as documento,\n" +
                            "d.nrodocto,d.neto,d.iva,d.total,d.categoria,d.subcategoria,d.rut,d.tipdocto,d.ccosto,d.trabajador\n" +
                            "from gastosrendiciondet d\n" +
                            "left join proveedor p on d.rut=p.rut\n" +
                            "left join gastos_det gd on d.subcategoria=gd.id\n" +
                            "where d.folio=" + Folio);
            fmMain.LimpiaGrilla(Model);
            while(Rs.next()){
                Model.addRow(new Object[]{
                        Rs.getString("concepto").trim(),
                        Rs.getString("nombre").trim(),
                        Rs.getDate("femision"),
                        Rs.getString("documento").trim(),
                        Rs.getInt("nrodocto"),
                        Rs.getDouble("neto"),
                        Rs.getDouble("iva"),
                        Rs.getDouble("total"),
                        Rs.getInt("categoria"),
                        Rs.getInt("subcategoria"),
                        Rs.getInt("rut"),
                        Rs.getString("tipdocto").trim()
                        
                });
            }
            Rs.first();
            cbCCosto.setEnabled(true);
            cbCCostoId.setSelectedItem(Rs.getString("ccosto").trim());
            cbCCosto.setSelectedIndex(cbCCostoId.getSelectedIndex());
            
            cbTrabajadorId.setSelectedItem(Rs.getString("trabajador").trim());
            cbTrabajador.setSelectedIndex(cbTrabajadorId.getSelectedIndex());
            
            PosCCosto = cbCCosto.getSelectedIndex();
            PosTrabajador = cbTrabajador.getSelectedIndex();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        } finally{
            Sql.Close();
        }
    }

