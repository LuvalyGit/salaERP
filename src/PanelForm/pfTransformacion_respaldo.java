    private void btTransformarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTransformarActionPerformed
        //Valida Stock
        if(txCantidad.getText().isEmpty())
            return;
                
        double Cant=Integer.valueOf(txCantidad.getText());
        double Factor,Stock;
        String Aux;
        boolean Alcanza=true;
        
        for(int i=0;i<GrillaRelacion.getRowCount(); i++){
            Factor =  Double.valueOf(GrillaRelacion.getValueAt(i,3).toString());
            Stock =  Double.valueOf(GrillaRelacion.getValueAt(i,4).toString());
            if(Factor*Cant > Stock)
                Alcanza=false;
        }
        if(Alcanza == false){
            if(fmMain.OkCancel("Algunos productos madres quedarán con stock negativo. ¿Desea realizar la transformación?")== JOptionPane.OK_OPTION){
                ExeSql Sql = new ExeSql();
                try {
                    Sql.ExeSql("select * from fn_transformacion('"+ txSku.getText().trim() +"',"+ txCantidad.getText() +")");
                    Sql.Commit();
                    fmMain.Mensaje("Transformado");
                    btIr.doClick();
                } catch (Exception e) {
                    fmMain.Mensaje(e.getMessage());
                    Sql.Rollback();
                }finally{
                    Sql.Close();
                }
            }
        }
        else if(fmMain.OkCancel("¿Desea realizar la transformación?")== JOptionPane.OK_OPTION){
            ExeSql Sql = new ExeSql();
                try {
                    Sql.ExeSql("select * from fn_transformacion('"+ txSku.getText().trim() +"',"+ txCantidad.getText() +")");
                    Sql.Commit();
                    fmMain.Mensaje("Transformado");
                    btIr.doClick();
                } catch (Exception e) {
                    fmMain.Mensaje(e.getMessage());
                    Sql.Rollback();
                }finally{
                    Sql.Close();
                }
        }
            
        
        
    }//GEN-LAST:event_btTransformarActionPerformed
    private void CargaProducto(String Codigo){
        DefaultTableModel TableModel = (DefaultTableModel) GrillaRelacion.getModel();
        ExeSql Sql = new ExeSql();
        ResultSet Rs;
        
        while(TableModel.getRowCount()>0)
              TableModel.removeRow(0);
        
        try {
            Rs = Sql.Select("select p.sku,p.nombre,u.unidad,i.stock\n" +
                            "from producto p\n" +
                            "left join par_unidad u on p.unidad=u.codigo\n" +
                            "left join inventario i on p.sku=i.sku\n" +
                            "where p.sku='"+ Codigo +"'");
            if(Sql.GetRowCount()>0){
                Rs.next();
                txSku.setText(Rs.getString("sku").trim());
                txNombre.setText(Rs.getString("nombre").trim());
                txUnidad.setText(Rs.getString("unidad").trim());
                txStock.setText(String.valueOf(Rs.getDouble("Stock")));
                
                Rs = Sql.Select("select t.skurel,p.nombre,u.unidad,t.cantidad,i.stock\n" +
                                "from transformacion t\n" +
                                "left join producto p on t.skurel=p.sku\n" +
                                "left join par_unidad u on p.unidad=u.codigo\n" +
                                "left join inventario i on p.sku=i.sku\n" +
                                "where t.sku='"+ Codigo +"'");
                if(Sql.GetRowCount()>0)
                    btTransformar.setEnabled(true);
                else
                    btTransformar.setEnabled(false);
                
                while(Rs.next()){
                    TableModel.addRow(new Object[]{
                                    Rs.getString("skurel").trim(),
                                    Rs.getString("nombre").trim(),
                                    Rs.getString("unidad").trim(),
                                    Rs.getDouble("cantidad"),
                                    Rs.getDouble("stock")
                    });
                }
                
            }
            else{
                txSku.setText("");
                txNombre.setText("");
                txUnidad.setText("");
                txStock.setText("");
            }
                
            
        } catch (Exception e) {
            System.out.println(e);
        } finally{
            Sql.Close();
        }
    }

